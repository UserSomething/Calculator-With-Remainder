    private void Arithmatic() {
        switch(operator) {
            case "/":
                result = doubleNum / Double.parseDouble(DisplayScreen.getText());
                DisplayScreen.setText(Double.toString(result));
            break;
            
            case "mod":
                result = doubleNum % Double.parseDouble(DisplayScreen.getText());
                DisplayScreen.setText(Double.toString(result));
            break;
            
            case "*":
                result = doubleNum * Double.parseDouble(DisplayScreen.getText());
                DisplayScreen.setText(Double.toString(result));
            break;
            
            case "-":
                result = doubleNum - Double.parseDouble(DisplayScreen.getText());
                DisplayScreen.setText(Double.toString(result));
            break;
            
            case "+":
                result = doubleNum + Double.parseDouble(DisplayScreen.getText());
                DisplayScreen.setText(Double.toString(result));
            break;
        }
    }
    
