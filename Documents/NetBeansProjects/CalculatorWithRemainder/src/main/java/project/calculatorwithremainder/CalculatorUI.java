    private void DivisionButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DivisionButtonActionPerformed
        doubleNum = Double.parseDouble(DisplayScreen.getText());
        operator = "/";
        DisplayScreen.setText("");
        //Checks if doubleNum does not have a decimal point.
        if((doubleNum % 1) != 0) {
            CalculationHistory.setText(doubleNum + " /");
        }
        else {
            CalculationHistory.setText((int) Math.round(doubleNum) + " /");
        }
    }//GEN-LAST:event_DivisionButtonActionPerformed

